
// Ascending Loop (made loop ascend by 3)
for (i=0; i<=99; i+=3){
    console.log(i);
}
// Decending Loop (made loop decend by 3)
for (i=99; i>=0; i-=3){
    console.log(i);
}
//Even Numbers Ascending
for (i=0; i<=98; i+=2){
    console.log(i);
}
//Even Numbers Decending
for (i=98; i>=0; i-=2){
    console.log(i);
}

//Ascending Loop from 49-72
for (i = 49; i<=72; i++){
    console.log(i)
}
//Decending Loop from 81-26
for (i = 81; i>=26; i--){
    console.log(i)
}